package com.google.code.christy.shopactivityservice;

public class ShopOverall
{
	private long shopId;
	
	private int score;
	
	private int perCapita;

	public ShopOverall()
	{
		super();
	}

	public long getShopId()
	{
		return shopId;
	}

	public void setShopId(long shopId)
	{
		this.shopId = shopId;
	}

	public int getScore()
	{
		return score;
	}

	public void setScore(int score)
	{
		this.score = score;
	}

	public int getPerCapita()
	{
		return perCapita;
	}

	public void setPerCapita(int perCapita)
	{
		this.perCapita = perCapita;
	}
	
	
}
